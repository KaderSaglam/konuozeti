package konuOzeti;

public class For {
    public static void main(String[] args) {

         //sayinin karakoku bulun
        System.out.println(Math.sqrt(49)); // math klosöründen yararlanarakta yapilabilir
//for i le carpim tablosu
        for (int i = 0; i <= 10; i++) {
            for (int j = 0; j <= 10; j++) {
                System.out.println(i +" x " +j +" = "+i*j);

            }

        }

        }
    }

